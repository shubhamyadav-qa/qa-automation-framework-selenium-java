package bill;



public class loop {


	

	public void printStarArrow() {//">"(just like)
		for (int i = 1; i <= 10; i++) {
			for (int s = 1; s <= i; s++) {
				System.out.print("*");

			}
			System.out.println("");
		}
		for (int a = 10; a >= 1; a--) {
			for (int s = 1; s <= a; s++) {
				System.out.print("*");
			}
			System.out.println("");
		}
	
	}}

