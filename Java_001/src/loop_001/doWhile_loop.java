package loop_001;


public class doWhile_loop {
	public static void main(String[] args) {
		
	

	        int i = 1;   // initialization

	        do {
	            System.out.println("Number: " + i);  // statement
	            i++;   // increment
	        } 
	        while (i <= 5);   // condition check
	    }
}

